/* Crie uma variável chamada numero
 Usando if, else e else, verifique:
 Se o número é 0
 Se o número é positivo
 Se o número é negativo
 */

 let numero = 20

 if ( numero === 0 ) {
    console.log("Este é Zero.")
 } else if ( numero > 0 ) {
    console.log("Este número é positivo.")
 } else {
    console.log("Este número é negativo.")
 }





 







